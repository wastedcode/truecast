import { readFileSync, rmSync } from "node:fs";
import { basename, dirname, join } from "node:path";
import type { CachedPersona } from "../cache/index.js";
import { type Config, paths } from "../config/index.js";
import type { Ledger, WriteOptions } from "../ledger/index.js";
import type { Persona } from "../persona/index.js";
import { copyTreeNoSymlinks } from "../safety/index.js";

const STAMP = "<!-- GENERATED by truecast — edit instance/, regenerate with `truecast sync`. -->";

/** "skills/run-a-rat/SKILL.md" → "run-a-rat" */
export function skillLeaf(rel: string): string {
  const parts = rel.split("/");
  return parts.length >= 2 ? (parts[parts.length - 2] as string) : basename(rel);
}

/** Generate the Claude Code ergonomic surface: the `@agent` subagent + the `/skill` folders. */
export function materialize(
  cached: CachedPersona,
  persona: Persona,
  config: Config,
  ledger: Ledger,
  opts: WriteOptions = {},
): void {
  const name = cached.name;
  const m = persona.manifest;
  const identity = readFileSync(join(cached.coreDir, m.identity), "utf8");
  const skillRefs = m.skills.map((s) => `${name}-${skillLeaf(s)}`);

  const frontmatter = [
    "---",
    `name: ${name}`,
    `description: ${m.description ?? `The ${name} persona.`}`,
    ...(m.modelHint ? [`model: ${m.modelHint}`] : []),
    ...(m.tools && m.tools.length > 0 ? [`tools: ${m.tools.join(", ")}`] : []),
    ...(skillRefs.length > 0 ? [`skills: ${skillRefs.join(", ")}`] : []),
    "---",
  ].join("\n");

  const body = `${frontmatter}

${STAMP}

${identity}

## Your corpus
Your craft is at \`core/\` (a symlink) and your job at \`instance/\`, under \`.truecast/agents/${name}/\`
in the project root. Read \`core/persona.toml\`, then \`Read\` the listed files by path (a direct Read is
transparent through the symlink). Read \`instance/mandate.md\` for your job here and \`instance/work.md\`
for accumulated lessons. To search, target \`core/\` and \`instance/\` explicitly — a bare \`rg .\` misses
the symlinked core.
`;

  ledger.writeFile(paths.claudeAgent(config, name), body, "agent", name, opts);

  // write the desired skills
  const desired = new Set<string>();
  for (const s of m.skills) {
    const destDir = paths.claudeSkillDir(config, name, skillLeaf(s));
    desired.add(destDir);
    ledger.assertWritable(destDir, name, opts);
    rmSync(destDir, { recursive: true, force: true });
    copyTreeNoSymlinks(dirname(join(cached.coreDir, s)), destDir); // core/skills/<leaf>/
    ledger.recordDir(destDir, "skill", name);
  }

  // prune skills this persona owns that the NEW version dropped (R5: no orphaned /skill left behind)
  for (const e of ledger.ownedBy(name)) {
    if (e.kind === "skill" && !desired.has(e.path)) ledger.removeOwned(e.path);
  }
}
